#include "libft.h"

int	ft_atoi(const char *str)
{
	long long	res;
	long long	sign;

	res = 0;
	sign = 1;
	while (*str == '\t' || *str == '\n' || *str == '\v' ||
		*str == '\f' || *str == '\r' || *str == ' ')
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		res = (10 * res) + ((*str) - '0');
		str++;
	}
	return (res * sign);
}
