#include "libft.h"

void	ft_putnbr(int n)
{
	int	cp_n;
	int	n_size;

	if (n < 0)
	{
		if (n == -2147483648)
		{
			ft_putstr("-2147483648");
			return ;
		}
		ft_putchar('-');
		n *= -1;
	}
	cp_n = n;
	n_size = 1;
	while ((cp_n /= 10) > 0)
		n_size *= 10;
	while (n_size > 0)
	{
		ft_putchar((n / n_size) + '0');
		n %= n_size;
		n_size /= 10;
	}
}
